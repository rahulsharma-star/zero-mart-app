export async function initI18n() {
  // Default to Hindi — most of our users read/speak Hindi & Marwari.
  const saved = (await AsyncStorage.getItem('zero_lang')) || 'hi';
  await i18n.use(initReactI18next).init({
    resources,
    lng: saved,
    fallbackLng: 'en',
    interpolation: { escapeValue: false },
  });
}

export async function setLanguage(lng: string) {
  await AsyncStorage.setItem('zero_lang', lng);
  await i18n.changeLanguage(lng);
}

export default i18n;
